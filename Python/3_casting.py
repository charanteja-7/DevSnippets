#casting

#typecasting into int // we can covert complex to int
x = int(1)  
y = int(2.8)
z = int("3")

print(x,y,z)

#typecasting into float // we can covert complex to int

x = float(1)     
y = float(2.8)   
z = float("3")   
w = float("4.2")

print(x,y,z,w)

#typecasting into complex

x = complex(1)  
y = complex(2.8)
z = complex("3")

print(x,y,z)
